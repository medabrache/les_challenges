#include <stdio.h>
#include <stdlib.h>

int main()
{
    int a, b;
    printf("please enter the value of a : ");
    scanf("%d",&a);
    printf("please enter the value of b:");
    scanf("%d",&b);
    printf("a+b=%d\n",a+b);
    printf("a-b=%d\n",a-b);
    printf("a*b=%d\n",a*b);
    printf("a%%b=%d\n",a%b);

    return 0;
}
